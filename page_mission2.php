            La mission était de développer un jeu interactif "Plus ou Moins" en Python, avec la capacité de stocker les résultats des joueurs 
            dans un fichier CSV, dans le but de renforcer les compétences en programmation et en gestion de fichiers.
            Pour ce faire, le jeu a été conçu pour proposer un nombre aléatoire que les joueurs doivent deviner, avec des indications "plus" 
            ou "moins" données à chaque tentative. L'algorithme Python a été utilisé pour gérer la logique du jeu, générer des nombres aléatoires 
            et suivre les essais des joueurs.
            Un fichier CSV a été intégré pour enregistrer les résultats des parties, incluant les noms des joueurs, le nombre de tentatives 
            et le temps écoulé. Cela a nécessité l'utilisation des bibliothèques Python pour la gestion des fichiers, en assurant une lecture 
            et une écriture efficaces des données.
            Ce projet a permis de développer une compréhension approfondie de la programmation orientée utilisateur, tout en explorant 
            des concepts essentiels comme l'aléatoire, la gestion des erreurs et le stockage persistant des données.
